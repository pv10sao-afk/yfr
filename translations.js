// ========== TRANSLATION HELPER ==========
// t(key, fallback?)
//   Повертає переклад для поточної мови.
//   Якщо ключ відсутній у поточній мові — намагається взяти з 'uk' (базова мова).
//   Якщо і там немає — повертає явний fallback або сам key (видимий індикатор проблеми).
//
// Використання:
//   t('noMana')                        // → 'Недостатньо мани!' (або uk-fallback)
//   t('someNewKey', 'Default text')    // → 'Default text' якщо ключ не перекладений

window.t = function (key, fallback) {
    const lang = window.currentLang || 'uk';
    const dict = window.TRANSLATIONS;
    if (!dict) return fallback !== undefined ? fallback : key;

    // Спочатку — поточна мова
    const currentVal = dict[lang]?.[key];
    if (currentVal !== undefined) return currentVal;

    // Fallback → базова мова 'uk'
    if (lang !== 'uk') {
        const ukVal = dict['uk']?.[key];
        if (ukVal !== undefined) return ukVal;
    }

    // Explicit fallback або сам ключ (щоб відразу побачити що переклад відсутній)
    return fallback !== undefined ? fallback : key;
};
